public class Creator {
    public static void main(String[] args) {
//        String can hold a string literal of any text
        String name = "Cole Webster";
//        int holds a whole number, that is positive, negative or zero
        int yearCreated = 1987;
//        double can hold large numbers and decimals
        double priceChange = 14.50;
//        boolean is true or false
        boolean isPositive = true;
//        Char can hold any character and must have single quotes
        char answered = 'A';
        System.out.println(name);
        System.out.println(yearCreated);
        System.out.println(priceChange);
        System.out.println(isPositive);
        System.out.println(answered);
    }
}
